// Biblioteca Matematica - Math

Math.abs(x)   // Retorna o valor absoluto de x.
Math.ceil(x)   // Arredonda o número x para cima para o próximo inteiro.
Math.floor(x)   // Arredonda o número x para baixo para o inteiro anterior.
Math.round(x)   // Arredonda o número x para o inteiro mais próximo.
Math.max(x1, x2, xn)   // Retorna o maior valor entre x1, x2, ..., xn.
Math.min(x1, x2, xn)   // Retorna o menor valor entre x1, x2, ..., xn.
Math.pow(base, expoente)   // Retorna a base elevada ao expoente.
Math.sqrt(x)   // Retorna a raiz quadrada de x.
Math.random()   // Retorna um número pseudoaleatório entre 0 (inclusive) e 1 (exclusivo).
Math.sin(x), Math.cos(x), Math.tan(x)   // Funções trigonométricas que retornam o seno, cosseno e tangente de x, em radianos.

