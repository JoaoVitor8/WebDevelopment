// typeof - Conferir qual é o Tipo Primitivo de uma Variavel

// ---------------------------------------------------------------------------------------------------

// String  -  Cadeia de Caracteres (Texto)

let stringSimples = 'String Simples'
let stringDuplas = "String Duplas"

console.log(typeof(stringSimples), typeof(stringDuplas))

// ---------------------------------------------------------------------------------------------------

// Number  -  Tudo que envolve Números

let numberInteiros = 123
let numberFloat = 3.5

console.log(typeof(numberInteiros), typeof(numberFloat))

// ---------------------------------------------------------------------------------------------------

// Boolean  -  Somente 2 valores

let booleanTrue = true
let booleanFalse = false

console.log(typeof(booleanTrue), typeof(booleanFalse))

// ---------------------------------------------------------------------------------------------------

let nulo = null //null  -  nulo
let indefinido  //undefined  -  indefinido

console.log(typeof(nulo), typeof(indefinido))

// ---------------------------------------------------------------------------------------------------

// Array  -  Agrupamento de dados

const lista = [1, 2, 3, 4, 5, 6, 7, 8, 9, 0]

console.log(typeof(lista))

// ---------------------------------------------------------------------------------------------------

// Object  -  Chave: Valor

const umObjeto = {chave1: "valor1", chave2: "valor2", chave3: "valor3", chave4: "valor4", chave5: "valor5"}

console.log(typeof(umObjeto))

